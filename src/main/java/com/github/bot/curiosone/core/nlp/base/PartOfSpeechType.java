package com.github.bot.curiosone.core.nlp.base;

/**
* Part Of Speech (POS) type.
*
* @author Andrea Rivitto && Eugenio Schintu
* @see https://en.oxforddictionaries.com/grammar/word-classes-or-parts-of-speech
*/
public enum PartOfSpeechType {
  /**
  * Adjective Phrase.
  */
  AP,

  /**
  * Adjective preposition phrase.
  */
  APP,

  /**
  * Adjective.
  */
  ADJ,

  /**
  * Adverb.
  */
  ADV,

  /**
  * Conjunction.
  */
  CONJ,

  /**
  * Determiner.
  */
  DET,

  /**
  * Interjections.
  * (bye, goodbye, hello, farewell, hi, so long excuse me, thanks, thank you,
  *  thanks a lot, sorry, pardon)
  */
  INTERJ,

  /**
  * Noun.
  */
  N,

  /**
  * Negation.
  */
  NEG,

  /**
  * Nominal part.
  */
  NP,

  /**
  * Noun preposition phrase.
  */
  NPP,

  /**
  * Number.
  */
  NUMB,

  /**
  * Preposition.
  */
  PREP,

  /**
  * Pronoun.
  */
  PRON,

  /**
  * Sentence.
  */
  S,

  /**
  * Unknown.
  */
  UNKN,

  /**
  * Verb.
  */
  V,

  /**
  * Verbal part.
  */
  VP,

  /**
  * Verb preposition phrase.
  */
  VPP
}
